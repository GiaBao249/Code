// No Pain No Gain!

void Solve()
{
    int n;
    cin >> n;
    ll s = 0;
    int i = 0;
    while (n >= pow(10, i))
    {
        s = pow(10, i);
        i++;
    }
    i--;
    n -= pow(10, i);
    cout << n << endl;
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin >> t;
    while (t--)
        Solve();
    return 0;
}